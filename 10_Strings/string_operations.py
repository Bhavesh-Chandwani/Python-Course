# Concatenation - Sum of two strings
text_1  = "John"
text_2 = "Doe"
print(text_1 + " " + text_2)

# Repetition - Repeat strings multiple times.
text = "Hii"
print((text + " ") * 3)
print(" ".join ([text] * 3)) 

# Membership(in, not in) - Checks if substring exists inside another
text = "Bhavesh"
print("s" in " Bhavesh")
print("a" not in "Bhavesh")